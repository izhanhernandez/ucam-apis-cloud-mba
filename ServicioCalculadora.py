import sys
import MicroservicioSuma as suma
import MicroservicioResta as resta
import MicroservicioMultiplicacion as mult

# ================================================================================================================================================ #
# MAIN FUNCTION
# ================================================================================================================================================ #

def servicioCalculadora(servicio, variable1, variable2):
    if servicio.lower() == "suma": return suma.suma(variable1, variable2)
    if servicio.lower() == "resta": return resta.resta(variable1, variable2)
    if servicio.lower() == "multiplicacion": return mult.mult(variable1, variable2)

if __name__ == '__main__':
    servicio = sys.argv[1]
    variable1 = sys.argv[2]
    variable2 = sys.argv[3]
    print(servicioCalculadora(servicio, variable1, variable2))

